-- Also add the get_bull_by_naab and search_bulls RPC functions that the code uses
CREATE OR REPLACE FUNCTION public.get_bull_by_naab(naab text)
RETURNS jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  result jsonb;
  bull_record record;
BEGIN
  SELECT * INTO bull_record
  FROM bulls_denorm
  WHERE code IS NOT NULL
    AND UPPER(REPLACE(REPLACE(REPLACE(code, ' ', ''), '-', ''), '.', ''))
      = UPPER(REPLACE(REPLACE(REPLACE(naab, ' ', ''), '-', ''), '.', ''))
  LIMIT 1;

  IF NOT FOUND THEN
    RETURN jsonb_build_object('found', false);
  END IF;

  RETURN jsonb_build_object(
    'found', true,
    'bull_id', bull_record.id,
    'code', bull_record.code,
    'name', bull_record.name,
    'registration', bull_record.registration,
    'birth_date', bull_record.birth_date,
    'sire_naab', bull_record.sire_naab,
    'mgs_naab', bull_record.mgs_naab,
    'mmgs_naab', bull_record.mmgs_naab,
    'company', bull_record.company,
    'hhp_dollar', bull_record.hhp_dollar,
    'tpi', bull_record.tpi,
    'nm_dollar', bull_record.nm_dollar,
    'cm_dollar', bull_record.cm_dollar,
    'fm_dollar', bull_record.fm_dollar,
    'gm_dollar', bull_record.gm_dollar,
    'f_sav', bull_record.f_sav,
    'ptam', bull_record.ptam,
    'cfp', bull_record.cfp,
