CREATE OR REPLACE FUNCTION public.search_bulls(q text, limit_count int DEFAULT 10)
RETURNS SETOF jsonb
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN QUERY
  SELECT jsonb_build_object(
    'bull_id', b.id,
    'code', b.code,
    'name', b.name,
    'registration', b.registration,
    'birth_date', b.birth_date,
    'sire_naab', b.sire_naab,
    'mgs_naab', b.mgs_naab,
    'mmgs_naab', b.mmgs_naab,
    'company', b.company,
    'hhp_dollar', b.hhp_dollar,
    'tpi', b.tpi,
    'nm_dollar', b.nm_dollar,
    'cm_dollar', b.cm_dollar,
    'fm_dollar', b.fm_dollar,
    'gm_dollar', b.gm_dollar,
    'f_sav', b.f_sav,
    'ptam', b.ptam,
    'cfp', b.cfp,
    'ptaf', b.ptaf,
    'ptaf_pct', b.ptaf_pct,
    'ptap', b.ptap,
    'ptap_pct', b.ptap_pct,
    'pl', b.pl,
    'dpr', b.dpr,
    'liv', b.liv,
    'scs', b.scs,
    'mast', b.mast,
    'met', b.met,
    'rp', b.rp,
    'da', b.da,
    'ket', b.ket,
    'mf', b.mf,
    'ptat', b.ptat,
    'udc', b.udc,
    'flc', b.flc,
    'sce', b.sce,
    'dce', b.dce,
    'ssb', b.ssb,
    'dsb', b.dsb,
    'h_liv', b.h_liv,
    'ccr', b.ccr,
    'hcr', b.hcr,
    'fi', b.fi,
    'bwc', b.bwc,
    'sta', b.sta,
    'str', b.str,
    'dfm', b.dfm,
    'rua', b.rua,
    'rls', b.rls,
    'rtp', b.rtp,
    'ftl', b.ftl,
    'rw', b.rw,
    'rlr', b.rlr,
    'fta', b.fta,
    'fls', b.fls,
    'fua', b.fua,
    'ruh', b.ruh,
    'ruw', b.ruw,
    'ucl', b.ucl,
    'udp', b.udp,
    'ftp', b.ftp,
    'rfi', b.rfi,
    'beta_casein', b.beta_casein,
    'kappa_casein', b.kappa_casein,
    'gfi', b.gfi
  )
  FROM bulls_denorm b
  WHERE
    b.code ILIKE '%' || q || '%'
    OR b.name ILIKE '%' || q || '%'
    OR b.registration ILIKE '%' || q || '%'
  LIMIT limit_count;
END;
$$;
